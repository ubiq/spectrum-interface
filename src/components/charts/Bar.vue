<script>
import { Bar, mixins } from 'vue-chartjs'
const { reactiveProp } = mixins

export default {
  extends: Bar,
  mixins: [reactiveProp],
  props: ['options', 'plugins'],
  mounted () {
    // Overwriting base render method with actual data.
    this.renderChart(this.chartData, this.options)

    if (this.plugins) {
      this.plugins.forEach((val) => {
        this.addPlugin(val)
      })
    }
  }
}
</script>
